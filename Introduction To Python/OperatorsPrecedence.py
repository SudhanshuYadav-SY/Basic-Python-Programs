print((3+4)*5)      #() have the highest priority in Python
print(3+4**5)       #Followed By Exponential Operator i.e. **
print(3+4*5)        #Followed By *,/,%,// they all have same precedence
print(3-4+5)        #Followed By +,- as it has the same precedence
print(4*3/2%4//3)
#Important Note: Same precedence operators have priority from left to right