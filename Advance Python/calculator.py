
#Created a module named calculator

def Addition(a,b):
    sum = a + b
    print("Addition of "+str(a)+" AND "+str(b)+" = "+str(sum))


def Subtraction(a,b):
    sub = a - b
    print("Subtraction of " + str(a) + " AND " + str(b) + " = " + str(sub))


def Product(a,b):
    product = a * b
    print("Product of " + str(a) + " AND " + str(b) + " = " + str(product))


def Division(a,b):
    divide = a / b
    print("Division of " + str(a) + " AND " + str(b) + " = " + str(divide))