s = {1,9,6,8}
print(s)                                        #Print the Original Set
print(type(s))                                  #Print Data-Type of Set
s.add(4)                                        #Add an element to Set
print(s)                                        #Print Updated Set
print(type(s))                                  #Print Data-Type of Set

S = frozenset({1,9,6,8})                        #Demonstration of Frozen Set
print(S)                                        #Print the Frozen Set
print(type(S))                                  #Print Data-Type of Frozen Set